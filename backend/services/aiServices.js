const generateResult = async (prompt) => {
    try {
        // If prompt is an array, join it; otherwise, use as is
        const input = Array.isArray(prompt) ? prompt.join(' ') : prompt;
        const result = await model.generateContent(input);
        // Optionally log result for debugging
        console.log("AI Response:", result.response.text()); // Debug log
        return result.response.text();
    } catch (error) {
        console.error("AI Service Error:", error);
        // Return error message in JSON format
        return JSON.stringify({ text: "Sorry, there was an error generating the result." });
    }
};

